#include <iostream>
using namespace std;

int main() {
    int x = 5;
    int y = 10;
    
    if (x < y) {
        cout << "x is less than y" << endl;
    } else {
        cout << "x is greater than or equal to y" << endl;
    }
    
    cout << "x = " << x << ", y = " << y << endl;
    
    return 0;
} 